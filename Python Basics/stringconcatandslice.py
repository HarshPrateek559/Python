mystr = "This is a beautifyl day"
#to print full string or part of string "string slicing"
print(mystr+"\n"+mystr[0:5])
#reversing string, -ve value denote the position of character from end of string
print(mystr[::-1])
#printing string while skipping characters
print(mystr[0::2])#last argument denote number of characters to be skipped
#second argument denote the end character of the desired new string. When blank it take complete string